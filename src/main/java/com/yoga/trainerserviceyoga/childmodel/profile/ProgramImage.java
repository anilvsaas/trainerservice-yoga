package com.yoga.trainerserviceyoga.childmodel.profile;

public class ProgramImage {
	private String programImg;
	private String imgPath;
	private boolean imageVerfied;

	public String getProgramImg() {
		return programImg;
	}

	public void setProgramImg(String programImg) {
		this.programImg = programImg;
	}

	public String getImgPath() {
		return imgPath;
	}

	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}

	public boolean isImageVerfied() {
		return imageVerfied;
	}

	public void setImageVerfied(boolean imageVerfied) {
		this.imageVerfied = imageVerfied;
	}

}
